	public static Throwable translateThrowable(Throwable throwable) {
		if (_useReflectionToTranslateThrowable) {
			try {
				UnsyncByteArrayOutputStream unsyncByteArrayOutputStream = new UnsyncByteArrayOutputStream();
				ObjectOutputStream objectOutputStream = new ObjectOutputStream(unsyncByteArrayOutputStream);

				objectOutputStream.writeObject(throwable);

				objectOutputStream.flush();
				objectOutputStream.close();

				UnsyncByteArrayInputStream unsyncByteArrayInputStream = new UnsyncByteArrayInputStream(unsyncByteArrayOutputStream.unsafeGetByteArray(),
						0, unsyncByteArrayOutputStream.size());

				Thread currentThread = Thread.currentThread();

				ClassLoader contextClassLoader = currentThread.getContextClassLoader();

				ObjectInputStream objectInputStream = new ClassLoaderObjectInputStream(unsyncByteArrayInputStream,
						contextClassLoader);

				throwable = (Throwable)objectInputStream.readObject();

				objectInputStream.close();

				return throwable;
			}
			catch (SecurityException se) {
				if (_log.isInfoEnabled()) {
					_log.info("Do not use reflection to translate throwable");
				}

				_useReflectionToTranslateThrowable = false;
			}
			catch (Throwable throwable2) {
				_log.error(throwable2, throwable2);

				return throwable2;
			}
		}

		Class<?> clazz = throwable.getClass();

		String className = clazz.getName();

		if (className.equals(PortalException.class.getName())) {
			return new PortalException();
		}

		if (className.equals(SystemException.class.getName())) {
			return new SystemException();
		}

		if (className.equals("org.kisti.edison.NoSuchCitationsException")) {
			return new org.kisti.edison.NoSuchCitationsException();
		}

		if (className.equals("org.kisti.edison.NoSuchContentsException")) {
			return new org.kisti.edison.NoSuchContentsException();
		}

		if (className.equals("org.kisti.edison.NoSuchExecuteUserException")) {
			return new org.kisti.edison.NoSuchExecuteUserException();
		}

		if (className.equals("org.kisti.edison.NoSuchScienceAppException")) {
			return new org.kisti.edison.NoSuchScienceAppException();
		}

		if (className.equals("org.kisti.edison.NoSuchSimulationJobException")) {
			return new org.kisti.edison.NoSuchSimulationJobException();
		}

		if (className.equals("org.kisti.edison.NoSuchSiteUserException")) {
			return new org.kisti.edison.NoSuchSiteUserException();
		}

		return throwable;
	}

